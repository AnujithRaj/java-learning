package basics;

public class NestedIfElse {
    public static void main(String[] args) {

       // nested If-Else
       int num =8;
       if (num >= 5) {
        if (num > 6) {
            System.out.println("Big Number");
        }
        else {
            System.out.println("Number is either 5 or 6");
        }
       }

    }
}
